package com.glockr.utils;

public class LoginParam {

	String SNAccessToken;
	String first_name;
	String email;
	String last_name;
	String SNUsername;
	String SNID;
	String appKey;
	String SNName;
	String app_ver;
	String device_token;
	
	public String getSNAccessToken() {
		return SNAccessToken;
	}

	public void setSNAccessToken(String sNAccessToken) {
		SNAccessToken = sNAccessToken;
	}

	public String getSNUsername() {
		return SNUsername;
	}

	public void setSNUsername(String sNUsername) {
		SNUsername = sNUsername;
	}

	public String getSNID() {
		return SNID;
	}

	public void setSNID(String sNID) {
		SNID = sNID;
	}

	public String getAppKey() {
		return appKey;
	}

	public void setAppKey(String appKey) {
		this.appKey = appKey;
	}

	public String getSNName() {
		return SNName;
	}

	public void setSNName(String sNName) {
		SNName = sNName;
	}

	public String getDevice_token() {
		return device_token;
	}

	public void setDevice_token(String device_token) {
		this.device_token = device_token;
	}

	public String getApp_ver() {
		return app_ver;
	}

	public void setApp_ver(String app_ver) {
		this.app_ver = app_ver;
	}

	
	public String getFirst_name() {
		return first_name;
	}

	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getLast_name() {
		return last_name;
	}

	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}

}


